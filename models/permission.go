package models

type Permission int

const (
	DECISION_READ Permission = iota
	DECISION_CREATE
	INGESTION
	SCENARIO_READ
	SCENARIO_CREATE
	SCENARIO_PUBLISH
	ORGANIZATIONS_LIST
	ORGANIZATIONS_CREATE
	USER_CREATE
)

func (r Permission) String() string {
	return [...]string{"DECISION_READ", "DECISION_CREATE", "INGESTION", "SCENARIO_READ", "SCENARIO_CREATE", "SCENARIO_PUBLISH", "ORGANIZATIONS_LIST", "ORGANIZATIONS_CREATE", "USER_CREATE"}[r]
}
